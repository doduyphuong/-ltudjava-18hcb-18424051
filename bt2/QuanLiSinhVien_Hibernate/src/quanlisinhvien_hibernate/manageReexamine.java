    private void initLayout() {
        TableReexamine tReexamine = TableReexamineDAO.checkTableReeaxamineExist(idTableReexamine);
        if (tReexamine != null) {
            String t = tReexamine.getNamHoc() + "-" + tReexamine.getHocKy();
            this.jHeader.setText(t);
        }
        List<Reexamine> listReexamine = ReexamineDAO.getListReexamine(idTableReexamine);
        if (listReexamine.size() > 0) {
            int stt = 1;
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.setColumnIdentifiers(columnNames);
            // "STT", "ID", "MSSV", "Họ Tên", "Môn Học", "Cột Điểm", "Điểm Đề Nghị", "Mô Tả", "Trạng Thái"
            for (Reexamine re : listReexamine) {
                String[] rows = new String[9];
                rows[0] = String.valueOf(stt);
                rows[1] = String.valueOf(re.getId());
                rows[2] = re.getMssv();
                rows[3] = re.getName();
                rows[4] = re.getCourse();
                rows[5] = re.getRowScore();
                rows[6] = re.getSuggestScore().toString();
                rows[7] = re.getDescription();
                rows[8] = re.getStatus();

                tableModel.addRow(rows);
                stt++;
            }

            jTableReexamine.setModel(tableModel);
        } else {
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.setColumnIdentifiers(columnNames);
            jTableReexamine.setModel(tableModel);
        }
    }

