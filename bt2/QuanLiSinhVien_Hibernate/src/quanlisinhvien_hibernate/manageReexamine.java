    private void jViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jViewActionPerformed
        // TODO add your handling code here:
        int selectRow = jTableReexamine.getSelectedRow();
        if (selectRow >= 0) {
            //"STT", "ID", "MSSV", "Họ Tên", "Môn Học", "Cột Điểm", "Điểm Đề Nghị", "Mô Tả", "Trạng Thái"
            String id = (String) jTableReexamine.getValueAt(selectRow, 1);
            this.jtxtID.setText(id);
            
            String mssv = (String) jTableReexamine.getValueAt(selectRow, 2);
            this.jtxtMSSV.setText(mssv);
            
            String name = (String) jTableReexamine.getValueAt(selectRow, 3);
            this.jtxtName.setText(name);
            
            String course = (String) jTableReexamine.getValueAt(selectRow, 4);
            this.jtxtCourse.setText(course);
            
            String rowScore = (String) jTableReexamine.getValueAt(selectRow, 5);
            this.jtxtRowScore.setText(rowScore);
            
            String suggetScore = (String) jTableReexamine.getValueAt(selectRow, 6);
            this.jtxtSuggestScore.setText(suggetScore);
            
            String description = (String) jTableReexamine.getValueAt(selectRow, 7);
            this.jDescription.setText(description);
            
            String status = (String) jTableReexamine.getValueAt(selectRow, 8);
            this.jcbStatus.setSelectedItem(status);
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn dòng để thực hiện", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jViewActionPerformed

    private void jbtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUpdateActionPerformed
        // TODO add your handling code here:
        int id = Integer.valueOf(this.jtxtID.getText());
        Reexamine re = ReexamineDAO.getReexamine(id);
        if(re != null) {
            String status = (String) jcbStatus.getItemAt(jcbStatus.getSelectedIndex());
            re.setStatus(status);
            boolean checkUpdate = ReexamineDAO.updateReexamine(re);
            if(checkUpdate) {
                JOptionPane.showMessageDialog(null, "Cập nhật thành công.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Xảy ra lỗi cập nhật.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
            initLayout();
        } else {
            JOptionPane.showMessageDialog(null, "Xảy ra lỗi cập nhật.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            initLayout();
        }
    }//GEN-LAST:event_jbtnUpdateActionPerformed

