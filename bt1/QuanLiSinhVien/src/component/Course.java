package component;

/**
 *
 * @author CPU12407-local
 */
public class Course {
    private String id;
    private String name;
    
    public Course (String _id, String _name) {
        this.id = _id;
        this.name = _name;
    }
    
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
